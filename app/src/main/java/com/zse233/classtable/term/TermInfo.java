package com.zse233.classtable.term;

public class TermInfo {
    private int code;
    private String name;

    public TermInfo(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public String getName() {
        return name;
    }
}
